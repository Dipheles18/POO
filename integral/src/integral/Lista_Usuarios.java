    private void jtUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtUsuariosMouseClicked
      formUsuarios.setVisible(true);
        buscar.setText("");
        ResultSet rs = null;
        try {
            connBD op = new connBD();
            op.Conectar();
            int Fila = jtUsuarios.getSelectedRow();
            String codigo = jtUsuarios.getValueAt(Fila, 0).toString();
            String sql = "SELECT * FROM usuarios where nUsuario='"+codigo+"'";
            op.rs = op.st.executeQuery(sql);
            while (op.rs.next()) {
                numu.setText(op.rs.getString("nUsuario"));
                nom.setText(op.rs.getString("nombreUsuario"));
                correo.setText(op.rs.getString("Correo_electronico"));
                clave.setText(op.rs.getString("Clave"));
                encri.setText(op.rs.getString("encriptado"));

            }

        } catch (SQLException ex) {

            System.out.println(ex.toString());

        }
    }//GEN-LAST:event_jtUsuariosMouseClicked

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
         connBD op = new connBD();
        try{

            op.Conectar();
            String sql = "update usuarios set nombreUsuario='"+nom.getText()+"', Correo_electronico='"+
            correo.getText()+"', encriptado='"+
            encri.getText()+"', Clave='"+
            clave.getText()+"' where nUsuario="+numu.getText();
            int executeUpdate;
            executeUpdate = op.st.executeUpdate(sql);
            if(executeUpdate>0){

                formUsuarios.setVisible(false);

                numu.setText("");

                nom.setText("");

                correo.setText("");

                clave.setText("");
                encri.setText("");

                boton_lupa.doClick();

            }
        }catch(SQLException ex){
            System.out.println("Ha habido un fallo en la conexion a la BD : "+ex.getMessage());
        }finally{
            try{
                op.conexion.close();
                op.st.close();
            }catch(SQLException ex){}
        }
    }//GEN-LAST:event_ModificarActionPerformed

